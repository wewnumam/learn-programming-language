#include <stdio.h>

int main(int argc, char const *argv[])
{
    printf("\\ \n");     // \ character
    printf("\' \n");     // ' character
    printf("\" \n");     // " character
    printf("\? \n");     // ? character
    printf("alert\a \n");     // Alert or bell
    printf("backspace\b \n");     // Backspace
    printf("\f form feed\n");     // Form feed
    printf("\n newline\n");     // Newline
    printf("\r carriage return\n");     // Carriage return
    printf("\t horizontal tab\n");     // Horizontal tab
    printf("\v vertical tab\n");     // Vertical tab
    printf("\x12 hexadecimal\n");   // . . . Hexadecimal number of one or more digits
    return 0;
}
